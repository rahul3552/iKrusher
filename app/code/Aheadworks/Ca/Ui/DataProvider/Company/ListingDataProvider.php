namespace Aheadworks\Ca\Ui\DataProvider\Company;

use Magento\Framework\Api\FilterBuilder;
use Magento\Framework\Api\Search\SearchCriteriaBuilder;
use Magento\Framework\App\RequestInterface;
use Magento\Framework\View\Element\UiComponent\DataProvider\Reporting;
use Magento\Framework\ObjectManagerInterface;
use Aheadworks\Ca\Ui\DataProvider\ListingDataProvider as GeneralListingDataProvider;

/**
 * Class ListingDataProvider
 *
 * This class is used to activate plugins, since virtual type is not suitable
 *
 * @package Aheadworks\Ca\Ui\DataProvider\Company
 */
class ListingDataProvider extends GeneralListingDataProvider
{
    /**
     * @param string $name
     * @param string $primaryFieldName
     * @param string $requestFieldName
     * @param Reporting $reporting
     * @param SearchCriteriaBuilder $searchCriteriaBuilder
     * @param RequestInterface $request
     * @param FilterBuilder $filterBuilder
     * @param ObjectManagerInterface $objectManager
     * @param array $meta
     * @param array $data
     * @SuppressWarnings(PHPMD.ExcessiveParameterList)
     */
    public function __construct(
        $name,
        $primaryFieldName,
        $requestFieldName,
        Reporting $reporting,
        SearchCriteriaBuilder $searchCriteriaBuilder,
        RequestInterface $request,
        FilterBuilder $filterBuilder,
        ObjectManagerInterface $objectManager,
        array $meta = [],
        array $data = []
    ) {
        //Virtual type instance
        $modifierPool = $objectManager->create(
            \Aheadworks\Ca\Ui\DataProvider\Company\Listing\Modifier\Pool::class
        );
        parent::__construct(
            $name,
            $primaryFieldName,
            $requestFieldName,
            $reporting,
            $searchCriteriaBuilder,
            $request,
            $filterBuilder,
            $modifierPool,
            $meta,
            $data
        );
    }
}
